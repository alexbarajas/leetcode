import collections
from typing import List


def getFood(grid: List[List[str]]) -> int:
    ROWS = len(grid)
    COLUMNS = len(grid[0])
    DIRECTIONS = [(1, 0), (0, 1), (-1, 0), (0, -1)]
    steps = 1

    queue = collections.deque()

    for x in range(ROWS):
        for y in range(COLUMNS):
            if grid[x][y] == "*":
                queue.append((x, y))
                break

    while queue:
        for i in range(len(queue)):
            x, y = queue.popleft()
            for dx, dy in DIRECTIONS:
                new_x = x + dx
                new_y = y + dy
                if 0 <= new_x < ROWS and 0 <= new_y < COLUMNS and grid[new_x][new_y] != "X":
                    if grid[new_x][new_y] == "#":
                        return steps
                    grid[new_x][new_y] = "X"
                    queue.append((new_x, new_y))
        steps += 1

    return -1


